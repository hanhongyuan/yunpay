package com.yunpay.permission.entity;

public class TranType {
	
	//交易类型码
	private String tranType;
	//交易类型名称
	private String tranDesc;
	//交易授权控制
	private String tranAuth;
	//交易类别
	private String tranKind;
	//交易处理标志
	private String tranPFlag;
	//交易码状态
	private String tranStat;
	//交易借贷标志
	private String tranCd;
	//结算标志
	private String settleFlag;
	//原始交易类型
	private String origTranType;
	//应用名称
	private String appType;
	
	
	
	public String getTranType() {
		return tranType;
	}
	public void setTranType(String tranType) {
		this.tranType = tranType;
	}
	public String getTranDesc() {
		return tranDesc;
	}
	public void setTranDesc(String tranDesc) {
		this.tranDesc = tranDesc;
	}
	public String getTranAuth() {
		return tranAuth;
	}
	public void setTranAuth(String tranAuth) {
		this.tranAuth = tranAuth;
	}
	public String getTranKind() {
		return tranKind;
	}
	public void setTranKind(String tranKind) {
		this.tranKind = tranKind;
	}
	public String getTranPFlag() {
		return tranPFlag;
	}
	public void setTranPFlag(String tranPFlag) {
		this.tranPFlag = tranPFlag;
	}
	public String getTranStat() {
		return tranStat;
	}
	public void setTranStat(String tranStat) {
		this.tranStat = tranStat;
	}
	public String getTranCd() {
		return tranCd;
	}
	public void setTranCd(String tranCd) {
		this.tranCd = tranCd;
	}
	public String getSettleFlag() {
		return settleFlag;
	}
	public void setSettleFlag(String settleFlag) {
		this.settleFlag = settleFlag;
	}
	public String getOrigTranType() {
		return origTranType;
	}
	public void setOrigTranType(String origTranType) {
		this.origTranType = origTranType;
	}
	public String getAppType() {
		return appType;
	}
	public void setAppType(String appType) {
		this.appType = appType;
	}
}
