	  private Integer status;//门店状态，0：停用，1：启用
	  private Integer apprStatus;//审核状态 0：未送审 1：审核中 2：审核通过 3：驳回 4：回退
	  private String apprRemark;//审核意见
	  private Date createdAt;//创建时间
	  private Integer createdBy;//创建人userId
	  private Date updatedAt;//更新时间
	  private Integer updatedBy;//更新人userId
	  private Integer informationSources;//信息来源，0：平台系统，1：商户系统
	  private String registerName;//商户公司名称
	  
	  
